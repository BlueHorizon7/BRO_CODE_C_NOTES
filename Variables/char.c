#include <stdio.h>

int main() {
    char grade = 'A';
    char symbol = '!';
    char currency = '$';

    // char means character, like string but not exactly. A char stores only one letter, number or symbols. Always use single quotes(') for char.

    // These are char they store a single character

    printf("Your grade is %c\n", grade);
    printf("You symbol is %c\n", symbol);
    printf("Your currency %c\n", currency);

    return 0;
}